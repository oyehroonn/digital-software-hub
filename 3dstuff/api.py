    folder = m.get('folder', '')
    pid = str(m.get('id', ''))

    # Try to get real data from Excel
    excel = EXCEL_DATA.get(pid, {})

    # ── Categories: use Excel 'categories' (may be comma-separated) ──
    raw_categories = excel.get('categories', '').strip()
    if raw_categories:
        categories_list = [c.strip() for c in raw_categories.split(',') if c.strip()]
    else:
        # Fallback: infer from name
        categories_list = ['Software']
        if any(x in name.lower() for x in ['office', 'word', 'excel', 'powerpoint', 'outlook']):
            categories_list = ['Microsoft Office']
        elif any(x in name.lower() for x in ['autocad', 'revit', 'maya', '3ds max', 'civil 3d']):
            categories_list = ['Architecture and Engineer']
        elif any(x in name.lower() for x in ['adobe', 'photoshop', 'illustrator', 'premiere', 'creative cloud']):
            categories_list = ['Adobe']
        elif any(x in name.lower() for x in ['windows', 'server', 'sql']):
            categories_list = ['Windows']
        elif any(x in name.lower() for x in ['kaspersky', 'security', 'antivirus']):
            categories_list = ['Security']
    # Primary category is the first one
    category = categories_list[0] if categories_list else 'Software'

    # ── Brand: infer from ALL categories and name ──
    brand = 'Other'
    name_lower = name.lower()
    # Use individual category names (lowercased) for precise matching
    cats_lower_list = [c.lower() for c in categories_list]
    # Combined text only for substring checks where appropriate
    all_cats_lower = ' '.join(cats_lower_list)
    
    # Helper: check if ANY individual category starts with a prefix
    def any_cat_starts(prefix):
        return any(c.startswith(prefix) for c in cats_lower_list)
    # Helper: check if ANY individual category equals a value
    def any_cat_equals(val):
        return val in cats_lower_list
    
    # Autodesk-related category keywords (checked as substrings in cat names)
    autodesk_cat_keywords = ['autodesk', 'autocad', '3ds max', 'civil 3d', 'fusion 360',
                             'inventor', 'maya', 'naviswork', 'revit', 'aec collection',
                             'architecture and engineer', 'dmc', 'mec collection', 'infodrainage']
    
    # Check Adobe FIRST (before Microsoft) since "Office Applications" is a generic category
    if 'adobe' in name_lower or any_cat_equals('adobe'):
        brand = 'Adobe'
    elif any(kw in name_lower or kw in all_cats_lower for kw in autodesk_cat_keywords):
        brand = 'Autodesk'
    elif ('microsoft' in name_lower or any_cat_starts('microsoft') or
          any_cat_starts('windows') or any_cat_starts('sql server') or
          any_cat_equals('dynamics 365') or any_cat_starts('microsoft power bi') or
          any_cat_starts('microsoft visual studio') or any_cat_starts('microsoft visio') or
          any_cat_starts('microsoft project') or any_cat_starts('share point') or
          any_cat_starts('exchange server') or any_cat_starts('office 2') or
          any_cat_starts('microsoft office')):
        brand = 'Microsoft'
    elif 'chaos' in name_lower or 'v-ray' in name_lower or 'enscape' in name_lower or 'corona' in name_lower:
        brand = 'Chaos'
    elif 'sketchup' in name_lower or any_cat_starts('sketchup'):
        brand = 'SketchUp'
    elif 'kaspersky' in name_lower:
        brand = 'Kaspersky'
    elif 'rhino' in name_lower:
        brand = 'Rhino'

    # ── License type: infer from product type or name ──
    license_type = 'Subscription'
    product_type = excel.get('type', '').lower()
    if 'lifetime' in name.lower() or 'perpetual' in name.lower():
        license_type = 'Lifetime'
    elif 'mak' in name.lower():
        license_type = 'MAK'
    elif 'cal' in name.lower() or 'device cal' in name.lower():
        license_type = 'Device CAL'
    elif product_type == 'variable':
        license_type = 'Variable'
    elif product_type == 'variation':
        license_type = 'Subscription'

    # ── Price: use real Excel price ──
    price = None
    regular_price = excel.get('regular_price', '')
    sale_price = excel.get('sale_price', '')
    if sale_price and sale_price.strip():
        try:
            price = f"AED {float(sale_price):,.2f}"
        except (ValueError, TypeError):
            pass
    if not price and regular_price and regular_price.strip():
        try:
            price = f"AED {float(regular_price):,.2f}"
        except (ValueError, TypeError):
            pass
    if not price:
        price = "Contact for pricing"

    # ── Description: use real short_description or full description ──
    short_desc = excel.get('short_description', '').strip()
    full_desc = excel.get('description', '').strip()
    # Strip HTML tags from descriptions
    import re as _re
    if short_desc:
        short_desc = _re.sub(r'<[^>]+>', '', short_desc).strip()

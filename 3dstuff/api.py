# Load .env file — try dotenv first, then manual parse as fallback
def _load_env_file():
    """Load .env file into os.environ, trying multiple approaches."""
    env_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), ".env")
    # Approach 1: python-dotenv
    try:
        from dotenv import load_dotenv
        load_dotenv(env_path)
        return
    except PermissionError:
        pass
    except ImportError:
        pass
    except Exception:
        pass
    # Approach 2: manual parse (handles permission issues with dotenv internals)
    try:
        with open(env_path, 'r') as f:
            for line in f:
                line = line.strip()
                if not line or line.startswith('#') or '=' not in line:
                    continue
                key, _, value = line.partition('=')
                key = key.strip()
                value = value.strip().strip('"').strip("'")
                if key and not os.environ.get(key):
                    os.environ[key] = value
    except Exception:
        pass

_load_env_file()



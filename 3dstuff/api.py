EXCEL_DATA = {}  # id → {price, category, tags, short_desc, description, sku, type, ...}

def load_excel_data():
    """Load real product data from products.xlsx using zipfile+xml (lightweight).
    Populates the global EXCEL_DATA dict keyed by product ID."""
    global EXCEL_DATA
    xlsx_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), "products.xlsx")
    if not os.path.exists(xlsx_path):
        print("⚠️  products.xlsx not found. Product data will use inferred values.")
        return

    try:
        import zipfile
        import xml.etree.ElementTree as ET

        z = zipfile.ZipFile(xlsx_path)
        # Read shared strings
        ns = {'s': 'http://schemas.openxmlformats.org/spreadsheetml/2006/main'}
        tree = ET.parse(z.open('xl/sharedStrings.xml'))
        strings = []
        for si in tree.findall('.//s:si', ns):
            text_parts = []
            for t in si.findall('.//s:t', ns):
                if t.text:
                    text_parts.append(t.text)
            strings.append(''.join(text_parts))

        # Read sheet data
        tree2 = ET.parse(z.open('xl/worksheets/sheet1.xml'))
        rows = tree2.findall('.//s:sheetData/s:row', ns)

        # Column mapping: A=ID, B=Image, C=SKU, D=Name, E=Type, F=Status,
        # G=Regular Price, H=Sale Price, I=Stock Status, J=Stock Quantity,
        # K=Categories, L=Tags, M=Short Description, N=Description

        for row in rows[1:]:  # skip header
            cells = {}
            for cell in row:
                ref = cell.get('r')
                col = ''.join(c for c in ref if c.isalpha())
                cell_type = cell.get('t')
                val_elem = cell.find('s:v', ns)
                if val_elem is not None:
                    val = val_elem.text
                    if cell_type == 's':
                        idx = int(val)
                        val = strings[idx] if idx < len(strings) else val
                    cells[col] = val

            pid = cells.get('A')
            if not pid:
                continue

            # Clean up HTML entities
            def clean_html(text):
                if not text:
                    return text
                return (text.replace('&amp;', '&')
                            .replace('&lt;', '<')
                            .replace('&gt;', '>')
                            .replace('&quot;', '"')
                            .replace('&#39;', "'"))

            EXCEL_DATA[str(pid)] = {
                'sku': cells.get('C', ''),
                'name': cells.get('D', ''),
                'type': cells.get('E', ''),
                'status': cells.get('F', ''),
                'regular_price': cells.get('G', ''),
                'sale_price': cells.get('H', ''),
                'stock_status': cells.get('I', ''),
                'stock_quantity': cells.get('J', ''),
                'categories': clean_html(cells.get('K', '')),
                'tags': clean_html(cells.get('L', '')),
                'short_description': clean_html(cells.get('M', '')),
                'description': clean_html(cells.get('N', '')),
            }

        z.close()
        print(f"✅ Loaded {len(EXCEL_DATA)} products from products.xlsx")
    except Exception as e:
        print(f"⚠️  Error reading products.xlsx: {e}")
        import traceback
        traceback.print_exc()


# Load Excel data at import time
